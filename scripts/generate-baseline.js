import fs from "node:fs";
import path from "node:path";

// Embed the global template JSONC into a JS module so the bundle carries a
// baseline configuration layer without depending on the user's filesystem
// (the installer drops the bundle and the templates in different locations,
// so runtime fs reads of the template are not portable).
//
// This script is invoked by both `scripts/build.js` (before esbuild) and the
// `npm test` driver (so source-mode tests have the generated module on disk).

const projectRoot = process.cwd();
const templatePath = path.join(
  projectRoot,
  "templates",
  "devai-aidd-plugin.global.jsonc",
);
const outputPath = path.join(
  projectRoot,
  "src",
  "config",
  "baseline-template.generated.js",
);

const templateText = fs.readFileSync(templatePath, "utf8");

const generated = `// AUTO-GENERATED by scripts/generate-baseline.js. Do not edit by hand.
// Source: templates/devai-aidd-plugin.global.jsonc
//
// Embedded baseline so a fresh install (no global/project JSONC on disk yet)
// still resolves a sensible effective config. The same template ships
// alongside the bundle and is copied to the user's config directory by the
// installer; users edit that file to override these values.
export const BASELINE_TEMPLATE_TEXT = ${JSON.stringify(templateText)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generated, "utf8");

console.log(
  `Generated ${path.relative(projectRoot, outputPath)} from ${path.relative(projectRoot, templatePath)}`,
);
