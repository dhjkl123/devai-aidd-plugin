import fs from "node:fs";
import path from "node:path";

// Build a single project-scope baseline by merging the shared base template
// with the project override template, then embed that JSONC into a JS module
// so the bundle carries a portable baseline configuration layer.
//
// This script is invoked by both `scripts/build.js` (before esbuild) and the
// `npm test` driver (so source-mode tests have the generated module on disk).

const projectRoot = process.cwd();
const globalTemplatePath = path.join(
  projectRoot,
  "templates",
  "devai-aidd-plugin.global.jsonc",
);
const projectTemplatePath = path.join(
  projectRoot,
  "templates",
  "devai-aidd-plugin.project.jsonc",
);
const outputPath = path.join(
  projectRoot,
  "src",
  "config",
  "baseline-template.generated.js",
);

function stripJsonComments(text) {
  return String(text || "")
    .replace(/^癤?/, "")
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/^\s*\/\/.*$/gm, "");
}

function mergeObjects(base, override) {
  if (!override || typeof override !== "object" || Array.isArray(override)) {
    return base;
  }

  const next = Array.isArray(base) ? [...base] : { ...base };
  for (const [key, value] of Object.entries(override)) {
    if (Array.isArray(value)) {
      next[key] = [...value];
      continue;
    }
    if (value && typeof value === "object") {
      const current =
        next[key] && typeof next[key] === "object" && !Array.isArray(next[key])
          ? next[key]
          : {};
      next[key] = mergeObjects(current, value);
      continue;
    }
    next[key] = value;
  }
  return next;
}

const globalTemplateText = fs.readFileSync(globalTemplatePath, "utf8");
const projectTemplateText = fs.readFileSync(projectTemplatePath, "utf8");
const mergedTemplate = mergeObjects(
  JSON.parse(stripJsonComments(globalTemplateText)),
  JSON.parse(stripJsonComments(projectTemplateText)),
);
const mergedTemplateText = `${JSON.stringify(mergedTemplate, null, 2)}\n`;

const generated = `// AUTO-GENERATED by scripts/generate-baseline.js. Do not edit by hand.
// Source: templates/devai-aidd-plugin.global.jsonc + templates/devai-aidd-plugin.project.jsonc
//
// Embedded baseline so a fresh install (no project JSONC on disk yet)
// still resolves a sensible effective config. The installer also emits the
// same merged JSONC as a project-local file.
export const BASELINE_TEMPLATE_TEXT = ${JSON.stringify(mergedTemplateText)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generated, "utf8");

console.log(
  `Generated ${path.relative(projectRoot, outputPath)} from ${path.relative(projectRoot, globalTemplatePath)} + ${path.relative(projectRoot, projectTemplatePath)}`,
);
