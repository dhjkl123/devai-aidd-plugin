/**
 * Story 4.2 — Legacy compatibility bridge service.
 *
 * Owns the lifecycle of the legacy mirror files that allow older readers of
 * `opencode-aidd-plugin.json` / `devai-git-workflow.json` to keep functioning
 * while the modern (`devai-aidd-plugin.project.jsonc`) configuration is the
 * source of truth.
 *
 * Story 1.3 intentionally left this responsibility unchanged when it
 * refactored `loadRuntimeConfig` into a deterministic merge pipeline (see
 * `_bmad-output/planning-artifacts/sprint-change-proposal-2026-05-08.md`,
 * Proposal C). Story 4.2 takes formal ownership and:
 *
 *   1. Moves the bridge body out of `src/config/` (which is now strictly a
 *      read-only resolver) into `src/services/compat/` per the architecture's
 *      Component Boundaries (config/ = merge + validate + migrate;
 *      services/compat/ = legacy bridge).
 *   2. Replaces the prior two-branch behaviour ("legacy without marker → leave
 *      alone, otherwise overwrite") with a deterministic decision table.
 *   3. Protects user-authored legacy files (no marker) from silent override
 *      (AC2 invariant).
 *   4. Makes writes idempotent — identical content is not rewritten so we do
 *      not churn `mtime` on every bootstrap (NFR1 latency).
 *   5. Shrinks the mirror payload to the keys the legacy reader actually
 *      uses (`branch` + `workflowPolicy`); modern-only sections such as
 *      `audit` are NOT mirrored (legacy readers don't know them).
 *   6. Returns a structured envelope `{ written, reason, paths? }` so the
 *      caller can emit a `compat.bridge.evaluated` audit event with full
 *      lifecycle context.
 *
 * Decision table (mirrors the table in Story 4.2 Tasks/Subtasks).
 *
 * Notation: `hasAnyUserLegacy = hasLegacyProject || hasLegacyWorkflow` —
 * either of the two legacy mirror files counts as a user asset signal when
 * the marker is absent (Story 4.2 R2 fix M-1: the original 7-case table
 * only covered `hasLegacyProject`; the workflow-only variant is now
 * symmetrically protected).
 *
 *   | Case | hasGlobal | hasProject | hasAnyUserLegacy | marker | result                                                 |
 *   |------|-----------|------------|------------------|--------|--------------------------------------------------------|
 *   | A    | false     | false      | false            | -      | no-op, reason="no-config-sources"                      |
 *   | B    | false     | false      | true             | false  | no-op, reason="preserve-existing-legacy"               |
 *   | C    | false     | false      | true             | true   | refresh, reason="refresh-bridge"                       |
 *   | D    | *         | true       | false            | false  | create, reason="create-bridge"                         |
 *   | E    | *         | true       | true             | true   | refresh, reason="refresh-bridge"                       |
 *   | E'   | *         | true       | false            | true   | rebuild, reason="rebuild-bridge" (R2 M-3 label fix)    |
 *   | F    | *         | true       | true             | false  | no-op, reason="preserve-user-legacy"  (AC2 guard)      |
 *   | G    | true      | false      | false            | *      | no-op, reason="global-only-no-bridge-needed"           |
 *
 * R2 M-3 note: when the marker survives but BOTH legacy files have been
 * deleted (manual `rm`), the bridge re-creates them. Reporting that as
 * `refresh-bridge` would be misleading because there is nothing to refresh,
 * so we tag it `rebuild-bridge` instead. Audit consumers can therefore
 * distinguish "in-place refresh" from "marker leftover, files missing".
 *
 * Idempotency: when a refresh/create/rebuild case computes content identical
 * to what is already on disk, the writes are skipped and the envelope
 * reports `written: false, reason: "no-content-change"`. The marker is also
 * written idempotently — its sentinel content is fixed.
 *
 * AC2 (no silent override) invariant: this service NEVER produces a mirror
 * file whose `branch`/`workflowPolicy` differ from the values the modern
 * effective config already chose. The mirror is *derived* from
 * `runtimeConfig.config` (which has already applied the precedence ordering
 * `DEFAULT → globalConfig → legacyProject → legacyWorkflowProject →
 * projectConfig`), so re-running `loadRuntimeConfig` after a mirror refresh
 * yields the same effective config. Tests
 * `verifyStory42BridgePrecedenceProjectOverridesLegacy` and
 * `verifyStory42BridgePreservesUserLegacyWithoutMarker` lock this in.
 */

const COMPAT_MARKER_CONTENT =
  "generated by devai-aidd-plugin to bridge legacy opencode runtime loading\n";

/**
 * Pick only the keys legacy readers consume. Modern-only sections (e.g.
 * `audit`) are intentionally omitted so a legacy reader cannot accidentally
 * see configuration shapes it does not understand.
 *
 * Reference shape: `templates/legacy-opencode-aidd-plugin.json` (branch +
 * workflowPolicy, no audit).
 */
function projectLegacyMirrorShape(config) {
  const mirror = {};
  if (config && typeof config === "object" && !Array.isArray(config)) {
    if (config.branch !== undefined) {
      mirror.branch = config.branch;
    }
    if (config.workflowPolicy !== undefined) {
      mirror.workflowPolicy = config.workflowPolicy;
    }
  }
  return mirror;
}

function serializeMirror(config) {
  return `${JSON.stringify(projectLegacyMirrorShape(config), null, 2)}\n`;
}

/**
 * Read existing file content if present, otherwise return null. Returns null
 * on read failure so a transient I/O error degrades to "rewrite" rather than
 * crashing bootstrap.
 */
function readIfExists(fsAdapter, filePath) {
  try {
    if (!fsAdapter.existsSync(filePath)) {
      return null;
    }
    return fsAdapter.readFileSync(filePath, "utf8");
  } catch {
    return null;
  }
}

/**
 * Write `content` to `filePath` only if the on-disk content differs. Returns
 * true if the file was actually written, false if skipped because the content
 * was already correct (idempotent path — protects mtime).
 *
 * Note (R2 L-3): the diff is byte-for-byte (`String === String`). BOM and
 * EOL differences (CRLF vs LF, leading UTF-8 BOM) are treated as a real
 * difference and trigger a rewrite. This is intentional — the bridge writes
 * deterministic LF-only, no-BOM content (`JSON.stringify(...) + "\n"`), so a
 * mirror file that differs in encoding/EOL was produced by something other
 * than the bridge and we want to canonicalize it.
 */
function writeIfChanged(fsAdapter, filePath, content) {
  const existing = readIfExists(fsAdapter, filePath);
  if (existing !== null && String(existing) === String(content)) {
    return false;
  }
  fsAdapter.writeFileSync(filePath, content, "utf8");
  return true;
}

function ensureTargetDirectory(fsAdapter, anyTargetPath) {
  const directory = fsAdapter.dirname(anyTargetPath);
  if (!fsAdapter.existsSync(directory)) {
    fsAdapter.mkdirSync(directory, { recursive: true });
  }
}

/**
 * Classify the input state into one of the seven decision-table cases. Pure:
 * input is the `runtimeConfig.sources` map plus the marker presence flag.
 *
 * Returns one of:
 *   { kind: "noop", reason }
 *   { kind: "write", reason }   // refresh OR create — the mirror payload
 *                                  shape is identical, so we collapse them
 *                                  into a single write branch and let the
 *                                  reason carry the semantic distinction.
 */
function classifyBridgeDecision(sources, markerPresent) {
  const hasGlobal = Boolean(sources.hasGlobalConfig);
  const hasProject = Boolean(sources.hasProjectConfig);
  const hasLegacyProject = Boolean(sources.hasLegacyProjectConfig);
  const hasLegacyWorkflow = Boolean(sources.hasLegacyWorkflowProjectConfig);
  // Story 4.2 R2 (M-1): treat EITHER legacy file as a "user asset" signal
  // when the marker is absent. The original table protected only the
  // `opencode-aidd-plugin.json` variant; the symmetric `devai-git-workflow.json`
  // variant fell into the defensive default and (a) was preserved only by
  // accident, (b) reported the misleading `no-config-sources` reason in
  // `compat.bridge.evaluated`. We now collapse the two file flags into a
  // single signal so the AC2 invariant ("compatibility support does not
  // silently override user intent") covers both files identically.
  const hasAnyUserLegacy = hasLegacyProject || hasLegacyWorkflow;

  // Case A: empty workspace — no signal that this directory uses the plugin.
  if (!hasGlobal && !hasProject && !hasAnyUserLegacy) {
    return { kind: "noop", reason: "no-config-sources" };
  }

  // Case G: global-only — no project-scoped signal, nothing to bridge into
  // a project workspace.
  if (hasGlobal && !hasProject && !hasAnyUserLegacy) {
    return { kind: "noop", reason: "global-only-no-bridge-needed" };
  }

  // Case F: modern + a user-authored legacy file (marker absent). AC2 guard —
  // refusing to overwrite preserves user intent. Order matters: F MUST be
  // evaluated before E so a user-authored legacy file is never mistaken for
  // a refreshable mirror. R2 M-1: covers BOTH `hasLegacyProject` and
  // `hasLegacyWorkflow` symmetrically.
  if (hasProject && hasAnyUserLegacy && !markerPresent) {
    return { kind: "noop", reason: "preserve-user-legacy" };
  }

  // Case E / E': modern + marker present.
  //   E  — at least one legacy mirror file exists → refresh in place.
  //   E' — both legacy mirror files have been deleted by the user but the
  //        marker leaked through → rebuild from scratch. R2 M-3: distinct
  //        reason label so audit can tell "in-place refresh" from
  //        "marker leftover, files missing".
  if (hasProject && markerPresent) {
    return {
      kind: "write",
      reason: hasAnyUserLegacy ? "refresh-bridge" : "rebuild-bridge",
    };
  }

  // Case D: modern only — bootstrap the mirror + marker for the first time.
  if (hasProject && !hasAnyUserLegacy) {
    return { kind: "write", reason: "create-bridge" };
  }

  // Case B: legacy only, no marker — user-authored legacy file we should not
  // touch. Bootstrap continues to read it via the modern read pipeline.
  // R2 M-1: covers both legacy file variants (see hasAnyUserLegacy comment).
  if (!hasProject && hasAnyUserLegacy && !markerPresent) {
    return { kind: "noop", reason: "preserve-existing-legacy" };
  }

  // Case C: legacy only, marker present — we own this mirror, refresh it
  // from the (legacy-derived) effective config.
  if (!hasProject && hasAnyUserLegacy && markerPresent) {
    return { kind: "write", reason: "refresh-bridge" };
  }

  // Defensive default. The enumerated cases above cover every input
  // combination given the boolean source flags, so reaching here would imply
  // a contract change in `runtimeConfig.sources`. Falling back to noop is
  // the safe choice (does not override anything, no audit-visible write).
  return { kind: "noop", reason: "no-config-sources" };
}

/**
 * Ensure legacy-compatible mirror files exist when (and ONLY when) the
 * decision table calls for a write. Returns an envelope:
 *
 *   {
 *     written: boolean,
 *     reason: string,         // one of the decision-table reasons or
 *                              "no-content-change" when an idempotent
 *                              skip turned a planned write into a no-op
 *     paths?: {
 *       legacyProjectConfigPath,
 *       legacyWorkflowProjectConfigPath,
 *       legacyCompatMarkerPath,
 *     },
 *     sources: { ...runtimeConfig.sources },
 *     markerPresent: boolean,
 *   }
 *
 * Backward compatibility: callers that relied on the prior boolean return
 * value will still see truthy/falsy via `envelope.written` if they coerce.
 * The bootstrap call site (`src/index.js`) ignores the value either way.
 */
export function ensureLegacyProjectConfigCompatibility(directory, fsAdapter, runtimeConfig) {
  // `directory` is reserved for the historical Story 1.3 signature so the
  // bootstrap call site (`src/index.js`) does not need to change when the
  // bridge moves. The function itself relies entirely on the already-resolved
  // absolute `paths` payload from `runtimeConfig` — a single source of truth
  // — to avoid silent mismatches if a future caller passes a directory that
  // diverges from `runtimeConfig.paths`. Do NOT add a second resolution
  // pathway here without re-checking the precedence invariant.
  void directory;

  const { config, paths, sources } = runtimeConfig;

  // R2 (L-2): short-circuit Case A before doing any disk syscalls. An empty
  // workspace is the hottest path during repeated test bootstraps and the
  // `existsSync(marker)` call is unnecessary there.
  const noUserSignal =
    !sources.hasGlobalConfig &&
    !sources.hasProjectConfig &&
    !sources.hasLegacyProjectConfig &&
    !sources.hasLegacyWorkflowProjectConfig;

  const baseSources = {
    hasGlobalConfig: Boolean(sources.hasGlobalConfig),
    hasProjectConfig: Boolean(sources.hasProjectConfig),
    hasLegacyProjectConfig: Boolean(sources.hasLegacyProjectConfig),
    hasLegacyWorkflowProjectConfig: Boolean(sources.hasLegacyWorkflowProjectConfig),
  };

  if (noUserSignal) {
    return {
      sources: baseSources,
      markerPresent: false,
      written: false,
      reason: "no-config-sources",
    };
  }

  const markerPresent = fsAdapter.existsSync(paths.legacyCompatMarkerPath);
  const decision = classifyBridgeDecision(sources, markerPresent);

  const baseEnvelope = {
    sources: baseSources,
    markerPresent,
  };

  if (decision.kind === "noop") {
    return {
      ...baseEnvelope,
      written: false,
      reason: decision.reason,
    };
  }

  // Write branch — refresh-bridge / create-bridge / rebuild-bridge.
  //
  // Story 4.2 R2 (M-2): the legacy bridge is a *secondary* lifecycle. A
  // disk-write failure here (EACCES on a read-only checkout, ENOSPC, EROFS,
  // antivirus locking the file, ...) MUST NOT take down plugin bootstrap.
  // We therefore wrap the write block and surface the failure via the
  // envelope so the caller (`src/index.js`) can emit a
  // `compat.bridge.evaluated` audit event with `reason: "write-failed"` and
  // continue. Same NFR7/NFR8 spirit as the audit emission itself.
  let mirrorContent;
  try {
    ensureTargetDirectory(fsAdapter, paths.legacyWorkflowProjectConfigPath);
    mirrorContent = serializeMirror(config);

    const wroteWorkflow = writeIfChanged(
      fsAdapter,
      paths.legacyWorkflowProjectConfigPath,
      mirrorContent,
    );
    const wroteProject = writeIfChanged(fsAdapter, paths.legacyProjectConfigPath, mirrorContent);
    const wroteMarker = writeIfChanged(
      fsAdapter,
      paths.legacyCompatMarkerPath,
      COMPAT_MARKER_CONTENT,
    );

    const wroteAnything = wroteWorkflow || wroteProject || wroteMarker;

    return {
      ...baseEnvelope,
      written: wroteAnything,
      reason: wroteAnything ? decision.reason : "no-content-change",
      paths: {
        legacyProjectConfigPath: paths.legacyProjectConfigPath,
        legacyWorkflowProjectConfigPath: paths.legacyWorkflowProjectConfigPath,
        legacyCompatMarkerPath: paths.legacyCompatMarkerPath,
      },
    };
  } catch (error) {
    // Best-effort path — surface the failure as data, never throw.
    const message = error && typeof error.message === "string" ? error.message : String(error);
    return {
      ...baseEnvelope,
      written: false,
      reason: "write-failed",
      error: message,
      paths: {
        legacyProjectConfigPath: paths.legacyProjectConfigPath,
        legacyWorkflowProjectConfigPath: paths.legacyWorkflowProjectConfigPath,
        legacyCompatMarkerPath: paths.legacyCompatMarkerPath,
      },
    };
  }
}

